package main

import "fmt"

func main() {
    // Создаем каналы для сигналов о завершении
    done1 := make(chan bool)
    done2 := make(chan bool)

    // Запускаем первую горутину
    go func() {
        fmt.Println("Начинается выполнение первой задачи...")
        // Имитируем длительную операцию
        for i := 0; i < 5; i++ {
            fmt.Println("Первая задача:", i)
        }
        fmt.Println("Первая задача завершена")
        done1 <- true // Отправляем сигнал о завершении
    }()

    // Запускаем вторую горутину
    go func() {
        fmt.Println("Начинается выполнение второй задачи...")
        // Имитируем длительную операцию
        for i := 0; i < 5; i++ {
            fmt.Println("Вторая задача:", i)
        }
        fmt.Println("Вторая задача завершена")
        done2 <- true // Отправляем сигнал о завершении
    }()

    // Главная горутина ждет сигналов о завершении
    fmt.Println("Главная горутина ожидает завершения задач...")
    <-done1
    <-done2
    fmt.Println("Все задачи выполнены. Главная горутина продолжает работу")
}
